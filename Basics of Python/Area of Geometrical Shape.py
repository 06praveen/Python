#Calculate the area of geometrical shapes
#Area of Circle
r=float(input("Enter te value of Radius = "))
pi=3.142
area_circle=pi*r**2
print("\nThe Area of Circle is = ",area_circle)

#Area of Square
side=float(input("\nEnter te value of side = "))
area_square=side**2
print("\nThe Area of Square is = ",area_square)

#Area of Rectangle
l=float(input("\nEnter te value of length = "))
b=float(input("\nEnter te value of breadth = "))
area_rectangle=l*b
print("\nThe Area of Rectangle is = ",area_rectangle)

#Area of Triangle
height=float(input("\nEnter te value of height = "))
